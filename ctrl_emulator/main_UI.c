static int panelBat;

int main (int argc, char *argv[])
{
	if (InitCVIRTE (0, argv, 0) == 0)
		return -1;	/* out of memory */
	if ((panelBat = LoadPanel (0, "main_UI.uir", PANEL_BAT)) < 0)
		return -1;
	DisplayPanel (panelBat);
	RunUserInterface ();
	DiscardPanel (panelBat);
	return 0;
}

